    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cedula;
        cedula = JOptionPane.showInputDialog(this, "Ingrese la cedula del empleado");
        boolean encontrado = false;
        try{
            for (int i = 0; i <agregar.mercado.getEmpleados().size() ; i++) {
                if(agregar.mercado.getEmpleados().get(i).getCedula().equals(cedula)){
                    mostrar = new MostrarEmpleado();
                    mostrar.setEmpleado(agregar.mercado.getEmpleados().get(i));
                    mostrar.setVisible(true);
                    encontrado = true;
                    break;
                }
            }
            if(!encontrado){
                JOptionPane.showMessageDialog(this, "No existe el empleado");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "No tiene empleados");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String departamento = JOptionPane.showInputDialog(this, "Ingrese el departamento");
        ArrayList<Empleado> deptos = new ArrayList<>();
        
        try{
            for (int i = 0; i < agregar.mercado.getEmpleados().size(); i++) {
                deptos.add(agregar.mercado.getEmpleados().get(i));

            }
            for (int i = 0; i < deptos.size(); i++) {
                for (int j = i; j <  deptos.size()-1; j++) {
                    if (deptos.get(i).getDepartamento().equals(deptos.get(j).getDepartamento())) {
                        deptos.remove(j);
                    }

                }
            }
            ArrayList<Empleado> escogido = new ArrayList<>();
            for (int i = 0; i < deptos.size(); i++) {
                if (deptos.get(i).getDepartamento().equals(departamento)) {
                    escogido.add(deptos.get(i));
                }
            }
            
            for (int i = 0; i < escogido.size(); i++) {
                suma = escogido.get(i).getSalario() + suma;
            }
            JOptionPane.showMessageDialog(this, "El salario de ese departamento es "+suma);

        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, "No tiene empleados");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String posicion = JOptionPane.showInputDialog(this, "Ingrese la posicion");
        ArrayList<Empleado> pos = new ArrayList<>(); 
        try {
             for (int i = 0; i < agregar.mercado.getEmpleados().size(); i++) {
                pos.add(agregar.mercado.getEmpleados().get(i));

            }
            for (int i = 0; i < pos.size(); i++) {
                for (int j = i; j <  pos.size()-1; j++) {
                    if (pos.get(i).getPosicion().equals(pos.get(j).getPosicion())) {
                        pos.remove(j);
                    }

                }
            }
            ArrayList<Empleado> escogido = new ArrayList<>();
            for (int i = 0; i < pos.size(); i++) {
                if (pos.get(i).getPosicion().equals(posicion)) {
                    escogido.add(pos.get(i));
                }
            }
            
            for (int i = 0; i < escogido.size(); i++) {
                empleados++;
            }
            JOptionPane.showMessageDialog(this, "La cantidad de empleados es "+empleados);
             
         }catch(Exception e) {
            JOptionPane.showMessageDialog(this, "No tiene empleados");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

